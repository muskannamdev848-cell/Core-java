package Loops;

import java.util.Scanner;

public class countnto1 {
    void main(){
        System.out.println("Reverse Counting from :");
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();


        for(int i =n ; i>=1 ; i--){
            System.out.println(i);


        }

    }
}
